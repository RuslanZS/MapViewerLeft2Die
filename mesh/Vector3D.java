package mesh;

public class Vector3D {
    // Открытые целочисленные координаты трехмерного вектора (X, Y, Z) [INDEX: 0.1.8]
    // В конвейере движка они представляют значения в формате фиксированной точки
    public int x;
    public int y;
    public int z;

    /**
     * Дефолтный конструктор: создает нулевой пространственный вектор (0, 0, 0) [INDEX: 0.1.8]
     */
    public Vector3D() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    /**
     * Конструктор с параметрами для быстрой инициализации пространственного вектора [INDEX: 0.1.8]
     * @param x Координата по оси ширины
     * @param y Координата по оси высоты
     * @param z Координата по оси глубины
     */
    public Vector3D(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    /**
     * Переопределенный системный метод для преобразования вектора в строку [INDEX: 0.1.8].
     * Выводит компоненты координат через пробел строго по нативной спецификации FernFlower [INDEX: 0.1.8].
     * Использовался разработчиками при отладке и записи векторов в логи.
     * @return Текстовое представление вектора в формате "X Y Z"
     */
    public String toString() {
        return this.x + " " + this.y + " " + this.z;
    }

    /**
     * Утилитарный метод для копирования значений из другого трехмерного вектора [INDEX: 0.1.8]
     * @param other Ссылка на копируемый вектор
     */
    public void copyFrom(Vector3D other) {
        if (other != null) {
            this.x = other.x;
            this.y = other.y;
            this.z = other.z;
        }
    }

    /**
     * Быстрое обнуление всех пространственных компонент текущего вектора [INDEX: 0.1.8]
     */
    public void setZero() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }
}
